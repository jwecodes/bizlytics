    # ── Multi-file: growth rates table ──────────────────────────


    def multi_growth_table(self, growth_rates: dict, max_cols: int = 8):
        """
        Rows: metric. Columns: period transitions (A->B, B->C, ...).
        """
        if not growth_rates:
            self.body_text("No growth rate data available.")
            return

        # Gather all period pairs from first column
        sample_col = next(iter(growth_rates))
        transitions = [f"{g['from']}->{g['to']}" for g in growth_rates[sample_col]]
        shown_trans = transitions[:5]

        name_w  = 52
        val_w   = min(26, int((190 - name_w) / max(1, len(shown_trans))))

        # Header
        self.set_font("Helvetica", "B", 8)
        self.set_fill_color(*self.DARK)
        self.set_text_color(*self.WHITE)
        self.cell(name_w, 8, "Metric", fill=True)
        for t in shown_trans:
            short = sanitize(t[:10])
            self.cell(val_w, 8, short, fill=True, align="C")
        self.ln()

        self.set_font("Helvetica", "", 8)
        cols = list(growth_rates.keys())[:max_cols]
        for idx, col in enumerate(cols):
            g_list = growth_rates[col][:5]
            fill_color = (248, 250, 255) if idx % 2 == 0 else (255, 255, 255)
            self.set_fill_color(*fill_color)
            self.set_text_color(*self.DARK)
            self.cell(name_w, 6,
                sanitize(col.replace("_", " ").title()[:28]),
                border="B", fill=True)
            for g in g_list:
                pct = g.get("growth_pct")
                if pct is not None:
                    try:
                        pct_f = float(pct)
                        self.set_text_color(*self.GREEN_TEXT if pct_f >= 0 else self.RED)
                        self.cell(val_w, 6, f"{pct_f:+.1f}%", border="B", fill=True, align="R")
                    except (TypeError, ValueError):
                        self.set_text_color(*self.GRAY)
                        self.cell(val_w, 6, "-", border="B", fill=True, align="R")
                else:
                    self.set_text_color(*self.GRAY)
                    self.cell(val_w, 6, "-", border="B", fill=True, align="R")
            self.ln()
        self.ln(4)


