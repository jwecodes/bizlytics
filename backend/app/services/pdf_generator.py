from fpdf import FPDF
from datetime import datetime


# ── Helpers ────────────────────────────────────────────────────────────────

def sanitize(text) -> str:
    if not isinstance(text, str):
        text = str(text)
    replacements = {
        "\u2014": "-", "\u2013": "-",
        "\u2018": "'", "\u2019": "'",
        "\u201c": '"', "\u201d": '"',
        "\u2026": "...", "\u2022": "-",
        "\u25b2": "^", "\u25bc": "v",
        "\u00e9": "e", "\u00e0": "a",
        "\u00fc": "u", "\u00f6": "o",
        "\u00e4": "a", "\u00f1": "n",
        "\u00df": "ss", "\u20ac": "EUR",
        "\u00a3": "GBP", "\u00a5": "JPY",
        "\u2190": "<-", "\u2192": "->",
        "\u2191": "^", "\u2193": "v",
    }
    for char, replacement in replacements.items():
        text = text.replace(char, replacement)
    text = text.encode("latin-1", errors="ignore").decode("latin-1")
    return text


def fmt_num(val) -> str:
    try:
        f = float(val)
        if abs(f) >= 1_000_000:
            return f"{f/1_000_000:.2f}M"
        if abs(f) >= 1_000:
            return f"{f:,.2f}"
        return f"{f:.2f}"
    except (TypeError, ValueError):
        return sanitize(str(val)) if val is not None else "N/A"


# ── PDF Class ──────────────────────────────────────────────────────────────

class BizlyticsPDF(FPDF):

    # ── Colour palette ──────────────────────────────────────────────────
    BLUE        = (59, 130, 246)
    BLUE_DARK   = (37,  99, 235)
    PURPLE      = (124, 58, 237)
    DARK        = (15,  23,  42)
    GRAY        = (100, 116, 139)
    LIGHT_GRAY  = (148, 163, 184)
    BG_BLUE     = (239, 246, 255)
    BG_PURPLE   = (245, 243, 255)
    BG_GREEN    = (236, 253, 245)
    BG_RED      = (254, 242, 242)
    BG_YELLOW   = (254, 252, 232)
    RED         = (220,  38,  38)
    ORANGE      = (217, 119,   6)
    GREEN_TEXT  = (5,  150,  105)
    WHITE       = (255, 255, 255)

    # ── Header / Footer ─────────────────────────────────────────────────

    def header(self):
        if self.page_no() == 1:
            return  # Cover page has no header
        self.set_fill_color(*self.DARK)
        self.rect(0, 0, 210, 14, "F")
        self.set_font("Helvetica", "B", 8)
        self.set_text_color(*self.WHITE)
        self.set_y(3)
        self.cell(0, 8, "BIZLYTICS  |  AI-Powered Business Intelligence", align="C")
        self.set_text_color(*self.DARK)
        self.set_y(18)

    def footer(self):
        if self.page_no() == 1:
            return
        self.set_y(-14)
        self.set_draw_color(*self.BLUE)
        self.set_line_width(0.5)
        self.line(10, self.get_y(), 200, self.get_y())
        self.set_font("Helvetica", "I", 8)
        self.set_text_color(*self.LIGHT_GRAY)
        self.cell(0, 10, f"Page {self.page_no() - 1}  |  Generated by Bizlytics  |  Confidential", align="C")

    # ── Cover page ──────────────────────────────────────────────────────

    def cover_page(self, domain_label: str, file_id: str, data_quality: dict, anomaly_count: int):
        # Full dark background
        self.set_fill_color(*self.DARK)
        self.rect(0, 0, 210, 297, "F")

        # Top accent bar
        self.set_fill_color(*self.BLUE)
        self.rect(0, 0, 210, 6, "F")

        # Logo area
        self.set_y(30)
        self.set_font("Helvetica", "B", 32)
        self.set_text_color(*self.WHITE)
        self.cell(0, 14, "Bizlytics", align="C", new_x="LMARGIN", new_y="NEXT")

        self.set_font("Helvetica", "", 12)
        self.set_text_color(*self.LIGHT_GRAY)
        self.cell(0, 8, "AI-Powered Business Intelligence", align="C", new_x="LMARGIN", new_y="NEXT")

        # Divider
        self.ln(10)
        self.set_draw_color(*self.BLUE)
        self.set_line_width(1)
        self.line(60, self.get_y(), 150, self.get_y())
        self.ln(14)

        # Report title
        self.set_font("Helvetica", "B", 22)
        self.set_text_color(*self.WHITE)
        self.cell(0, 12, "Business Analysis Report", align="C", new_x="LMARGIN", new_y="NEXT")

        self.ln(4)
        self.set_font("Helvetica", "", 13)
        self.set_text_color(147, 197, 253)
        self.cell(0, 8, sanitize(domain_label), align="C", new_x="LMARGIN", new_y="NEXT")

        # Stats cards row
        self.ln(20)
        stats = [
            ("Rows Analysed",  str(data_quality.get("total_rows", "N/A"))),
            ("Columns",        str(data_quality.get("total_columns", "N/A"))),
            ("Quality Score",  f"{data_quality.get('quality_score', 'N/A')}/100"),
            ("Anomalies",      str(anomaly_count)),
        ]
        card_w = 40
        total_w = card_w * len(stats) + 6 * (len(stats) - 1)
        start_x = (210 - total_w) / 2

        for i, (label, value) in enumerate(stats):
            x = start_x + i * (card_w + 6)
            # Card bg
            self.set_fill_color(30, 41, 59)
            self.rounded_rect(x, self.get_y(), card_w, 22, 3, "F")
            # Value
            self.set_font("Helvetica", "B", 14)
            self.set_text_color(*self.WHITE)
            self.set_xy(x, self.get_y() + 3)
            self.cell(card_w, 8, sanitize(value), align="C")
            # Label
            self.set_font("Helvetica", "", 7)
            self.set_text_color(*self.LIGHT_GRAY)
            self.set_xy(x, self.get_y() + 9)
            self.cell(card_w, 6, sanitize(label), align="C")

        self.ln(36)

        # File info
        self.set_font("Helvetica", "", 9)
        self.set_text_color(*self.LIGHT_GRAY)
        generated_at = datetime.now().strftime("%d %B %Y, %I:%M %p")
        self.cell(0, 6, sanitize(f"File ID: {file_id[:16]}...   |   Generated: {generated_at}"), align="C", new_x="LMARGIN", new_y="NEXT")

        # Bottom accent bar
        self.set_fill_color(*self.BLUE)
        self.rect(0, 291, 210, 6, "F")

    # ── Section helpers ─────────────────────────────────────────────────

    def section_title(self, title: str, color: tuple = None):
        self.ln(4)
        bg = color or self.BG_BLUE
        accent = self.BLUE if bg == self.BG_BLUE else (
            self.PURPLE if bg == self.BG_PURPLE else (
            self.RED if bg == self.BG_RED else self.ORANGE
        ))
        # Left accent bar
        self.set_fill_color(*accent)
        self.rect(10, self.get_y(), 3, 9, "F")
        # Title bg
        self.set_fill_color(*bg)
        self.rect(13, self.get_y(), 187, 9, "F")
        self.set_font("Helvetica", "B", 11)
        self.set_text_color(*self.DARK)
        self.set_x(16)
        self.cell(0, 9, sanitize(title), new_x="LMARGIN", new_y="NEXT")
        self.ln(3)
        self.set_text_color(*self.DARK)

    def sub_title(self, title: str):
        self.set_font("Helvetica", "B", 10)
        self.set_text_color(60, 80, 120)
        self.cell(0, 7, sanitize(title), new_x="LMARGIN", new_y="NEXT")
        self.ln(1)

    def body_text(self, text: str):
        self.set_font("Helvetica", "", 10)
        self.set_text_color(50, 65, 80)
        self.multi_cell(0, 6, sanitize(text))
        self.ln(2)

    def info_box(self, text: str, bg: tuple = None, text_color: tuple = None):
        bg = bg or self.BG_BLUE
        tc = text_color or self.DARK
        self.set_fill_color(*bg)
        self.set_font("Helvetica", "", 10)
        self.set_text_color(*tc)
        self.set_x(10)
        self.multi_cell(0, 7, sanitize(f"  {text}"), fill=True)
        self.ln(2)

    def rounded_rect(self, x, y, w, h, r, style=""):
        """Draw a rectangle with slightly rounded corners using lines+arcs."""
        self.rect(x, y, w, h, style)

    # ── Stat grid ───────────────────────────────────────────────────────

    def stat_grid(self, stats: list):
        """Render stats as a 3-column card grid."""
        col_w = 58
        gap   = 5
        per_row = 3
        start_x = 10

        for i, (label, value) in enumerate(stats):
            col = i % per_row
            if col == 0 and i > 0:
                self.ln(20)
            x = start_x + col * (col_w + gap)
            y = self.get_y()
            # Card
            self.set_fill_color(248, 250, 252)
            self.set_draw_color(226, 232, 240)
            self.set_line_width(0.3)
            self.rect(x, y, col_w, 16, "FD")
            # Value
            self.set_font("Helvetica", "B", 13)
            self.set_text_color(*self.BLUE_DARK)
            self.set_xy(x, y + 1)
            self.cell(col_w, 8, sanitize(str(value)), align="C")
            # Label
            self.set_font("Helvetica", "", 8)
            self.set_text_color(*self.GRAY)
            self.set_xy(x, y + 8)
            self.cell(col_w, 6, sanitize(label), align="C")
            self.set_xy(start_x, y)
        self.ln(22)

    # ── KPI table ───────────────────────────────────────────────────────

    def kpi_table(self, kpis: dict):
        col_w = [62, 30, 30, 30, 24, 14]
        headers = ["Metric", "Total", "Average", "Max", "Growth %", "Trend"]

        self.set_font("Helvetica", "B", 9)
        self.set_fill_color(*self.DARK)
        self.set_text_color(*self.WHITE)
        for i, h in enumerate(headers):
            self.cell(col_w[i], 9, h, fill=True, border=0, align="C")
        self.ln()

        self.set_font("Helvetica", "", 9)
        for idx, (name, kpi) in enumerate(list(kpis.items())[:20]):
            if name == "_domain_kpis" or not isinstance(kpi, dict):
                continue
            fill_color = (248, 250, 255) if idx % 2 == 0 else (255, 255, 255)
            self.set_fill_color(*fill_color)
            self.set_text_color(*self.DARK)

            trend    = kpi.get("trend", "-")
            trend_sym = "▲" if trend == "up" else ("▼" if trend == "down" else "—"  )
            trend_sym = "UP" if trend == "up" else ("DN" if trend == "down" else "-")
            growth   = kpi.get("growth_rate")
            growth_s = f"{growth:+.1f}" if isinstance(growth, (int, float)) else "-"

            # Colour growth cell
            row = [
                sanitize(name.replace("_", " ").title()[:34]),
                fmt_num(kpi.get("total")),
                fmt_num(kpi.get("average")),
                fmt_num(kpi.get("max")),
                growth_s,
                trend_sym,
            ]
            for i, cell_val in enumerate(row):
                if i == 4 and isinstance(growth, (int, float)):
                    self.set_text_color(*self.GREEN_TEXT if growth >= 0 else self.RED)
                elif i == 5:
                    self.set_text_color(*self.GREEN_TEXT if trend == "up" else (self.RED if trend == "down" else self.GRAY))
                else:
                    self.set_text_color(*self.DARK)
                align = "L" if i == 0 else "R"
                self.cell(col_w[i], 7, sanitize(str(cell_val)), border="B", fill=True, align=align)
            self.ln()
        self.set_draw_color(*self.LIGHT_GRAY)
        self.ln(4)

    # ── Domain KPI table ────────────────────────────────────────────────

    def domain_kpi_table(self, domain_kpis: dict):
        self.set_font("Helvetica", "B", 9)
        self.set_fill_color(*self.PURPLE)
        self.set_text_color(*self.WHITE)
        self.cell(100, 8, "Smart KPI", fill=True, border=0)
        self.cell(90,  8, "Value",     fill=True, border=0)
        self.ln()

        self.set_font("Helvetica", "", 9)
        for idx, (key, val) in enumerate(list(domain_kpis.items())[:10]):
            fill_color = (248, 245, 255) if idx % 2 == 0 else (255, 255, 255)
            self.set_fill_color(*fill_color)
            self.set_text_color(*self.DARK)
            self.cell(100, 7, sanitize(key.replace("_", " ").title()), border="B", fill=True)
            self.cell(90,  7, fmt_num(val), border="B", fill=True, align="R")
            self.ln()
        self.ln(4)

    # ── Insight cards ───────────────────────────────────────────────────

    def insight_cards(self, items: list, bg_color: tuple, accent: tuple = None):
        accent = accent or self.BLUE
        self.set_font("Helvetica", "", 10)
        for item in items[:6]:
            title = sanitize(item.get("title", ""))
            desc  = sanitize(item.get("description", ""))
            badge = (item.get("sentiment") or item.get("impact") or item.get("priority") or "").upper()

            badge_color = (
                self.GREEN_TEXT if badge in ("POSITIVE", "LOW")  else
                self.RED        if badge in ("NEGATIVE", "HIGH") else
                self.ORANGE
            )

            # Card background
            self.set_fill_color(*bg_color)
            self.set_draw_color(*accent)
            self.set_line_width(0.2)

            y_start = self.get_y()
            self.set_x(10)

            # Title line
            self.set_font("Helvetica", "B", 10)
            self.set_text_color(*self.DARK)
            title_text = sanitize(f"  {title}") + (f"  [{badge}]" if badge else "")
            self.set_fill_color(*bg_color)
            self.cell(0, 8, sanitize(title_text), fill=True, new_x="LMARGIN", new_y="NEXT")

            # Description
            self.set_font("Helvetica", "", 9)
            self.set_text_color(70, 85, 100)
            self.set_x(14)
            self.multi_cell(0, 5, desc)
            self.ln(2)

    # ── Anomaly card ────────────────────────────────────────────────────

    def anomaly_card(self, rc: dict, idx: int):
        self.set_fill_color(254, 242, 242)
        self.set_x(10)
        self.set_font("Helvetica", "B", 9)
        self.set_text_color(*self.RED)
        anomaly_types = rc.get("anomaly_types", ["numeric"])
        type_str = " + ".join(anomaly_types) if anomaly_types else "numeric"
        self.cell(0, 7, sanitize(f"  Row #{rc['row_index']}  [{type_str}]"),
                  fill=True, new_x="LMARGIN", new_y="NEXT")

        self.set_font("Helvetica", "", 9)
        self.set_text_color(60, 60, 60)
        for reason in rc.get("top_reasons", [])[:3]:
            col         = reason.get("column", "?")
            val         = reason.get("value", "?")
            reason_type = reason.get("type", "numeric")
            if reason_type == "categorical":
                reason_text = reason.get("reason", f"Unusual value '{val}' in column '{col}'")
                self.set_x(16)
                self.multi_cell(0, 5, sanitize(f"• {reason_text}"))
            else:
                normal_avg = reason.get("normal_avg")
                pct_diff   = reason.get("pct_diff")
                avg_s  = fmt_num(normal_avg) if normal_avg is not None else "N/A"
                diff_s = f"{pct_diff:+.1f}%" if isinstance(pct_diff, (int, float)) else "N/A"
                self.set_x(16)
                self.cell(0, 5,
                    sanitize(f"• {col}: {fmt_num(val)}  (avg: {avg_s},  deviation: {diff_s})"),
                    new_x="LMARGIN", new_y="NEXT")
        self.ln(2)

    # ── Correlation table ────────────────────────────────────────────────

    def correlation_table(self, correlations: dict):
        """Render top strong correlations as a simple table."""
        pairs = []
        for col_a, row in correlations.items():
            for col_b, val in row.items():
                if col_a >= col_b:
                    continue
                try:
                    pairs.append((col_a, col_b, float(val)))
                except (TypeError, ValueError):
                    pass
        pairs.sort(key=lambda x: abs(x[2]), reverse=True)

        if not pairs:
            self.body_text("No significant correlations found.")
            return

        # Table header
        self.set_font("Helvetica", "B", 9)
        self.set_fill_color(*self.DARK)
        self.set_text_color(*self.WHITE)
        self.cell(70, 8, "Column A",     fill=True)
        self.cell(70, 8, "Column B",     fill=True)
        self.cell(30, 8, "Correlation",  fill=True, align="C")
        self.cell(20, 8, "Strength",     fill=True, align="C")
        self.ln()

        self.set_font("Helvetica", "", 9)
        for idx, (a, b, val) in enumerate(pairs[:10]):
            fill_color = (248, 250, 252) if idx % 2 == 0 else (255, 255, 255)
            self.set_fill_color(*fill_color)

            strength = "Strong" if abs(val) >= 0.7 else ("Moderate" if abs(val) >= 0.4 else "Weak")
            if val >= 0.7:
                self.set_text_color(*self.GREEN_TEXT)
            elif val <= -0.7:
                self.set_text_color(*self.RED)
            else:
                self.set_text_color(*self.DARK)

            self.cell(70, 7, sanitize(a[:28]), border="B", fill=True)
            self.cell(70, 7, sanitize(b[:28]), border="B", fill=True)
            self.cell(30, 7, f"{val:+.3f}",   border="B", fill=True, align="C")
            self.set_text_color(*self.GRAY)
            self.cell(20, 7, strength,         border="B", fill=True, align="C")
            self.ln()
        self.ln(4)

    # ── Segment table ────────────────────────────────────────────────────

    def segment_table(self, segment_sizes: dict, total_rows: int):
        self.set_font("Helvetica", "B", 9)
        self.set_fill_color(*self.DARK)
        self.set_text_color(*self.WHITE)
        self.cell(40,  8, "Segment",    fill=True)
        self.cell(40,  8, "Row Count",  fill=True, align="C")
        self.cell(50,  8, "% of Total", fill=True, align="C")
        self.cell(60,  8, "Size",       fill=True, align="C")
        self.ln()

        colors = [
            (59, 130, 246), (168, 85, 247), (16, 185, 129),
            (245, 158, 11), (6, 182, 212), (236, 72, 153),
        ]
        self.set_font("Helvetica", "", 9)
        for idx, (seg, count) in enumerate(list(segment_sizes.items())[:8]):
            count_n = int(count)
            pct     = (count_n / total_rows * 100) if total_rows else 0
            color   = colors[idx % len(colors)]

            fill_color = (248, 250, 252) if idx % 2 == 0 else (255, 255, 255)
            self.set_fill_color(*fill_color)
            self.set_text_color(*self.DARK)
            self.cell(40, 7, sanitize(f"Segment {seg}"), border="B", fill=True)
            self.cell(40, 7, str(count_n), border="B", fill=True, align="C")
            self.cell(50, 7, f"{pct:.1f}%", border="B", fill=True, align="C")

            # Mini progress bar
            bar_x     = self.get_x() + 2
            bar_y     = self.get_y() + 1.5
            bar_total = 55
            bar_fill  = max(1, bar_total * pct / 100)
            self.set_fill_color(226, 232, 240)
            self.rect(bar_x, bar_y, bar_total, 4, "F")
            self.set_fill_color(*color)
            self.rect(bar_x, bar_y, bar_fill, 4, "F")
            self.cell(60, 7, "", border="B", fill=False)
            self.ln()
        self.ln(4)


# ── Main entry point ────────────────────────────────────────────────────────

def generate_pdf(
    domain:       dict,
    data_quality: dict,
    kpis:         dict,
    anomalies:    dict,
    insights:     dict,
    file_id:      str,
    forecast:     dict = None,
    ml_analysis:  dict = None,
) -> bytes:

    pdf = BizlyticsPDF()
    pdf.set_auto_page_break(auto=True, margin=22)

    domain_label = domain.get("label", "General Dataset") if domain else "General Dataset"
    anomaly_count = anomalies.get("anomaly_count", 0)

    # ── Page 1: Cover ──────────────────────────────────────────────────────
    pdf.add_page()
    pdf.cover_page(domain_label, file_id, data_quality, anomaly_count)

    # ── Page 2+: Content ───────────────────────────────────────────────────
    pdf.add_page()

    # ── Dataset Overview ────────────────────────────────────────────────
    pdf.section_title("Dataset Overview")
    stats = [
        ("Total Rows",      data_quality.get("total_rows",    "N/A")),
        ("Total Columns",   data_quality.get("total_columns", "N/A")),
        ("Quality Score",   f"{data_quality.get('quality_score', 'N/A')}/100"),
        ("Missing Data",    f"{data_quality.get('missing_pct', 'N/A')}%"),
        ("Duplicate Rows",  data_quality.get("duplicate_rows", 0)),
        ("Anomalies Found", anomaly_count),
    ]
    pdf.stat_grid(stats)

    # Missing values detail
    missing = data_quality.get("missing_values", {})
    high_missing = {k: v for k, v in missing.items() if isinstance(v, dict) and v.get("pct", 0) > 5}
    if high_missing:
        pdf.sub_title("Columns with High Missing Values (>5%)")
        pdf.set_font("Helvetica", "", 9)
        pdf.set_text_color(*pdf.GRAY)
        for col, info in list(high_missing.items())[:8]:
            pdf.cell(0, 5, sanitize(f"  • {col}: {info.get('count', 0)} missing ({info.get('pct', 0):.1f}%)"),
                     new_x="LMARGIN", new_y="NEXT")
        pdf.ln(3)

    # ── AI Executive Summary ─────────────────────────────────────────────
    if insights.get("executive_summary"):
        pdf.section_title("AI Executive Summary", pdf.BG_PURPLE)
        pdf.set_fill_color(*pdf.BG_PURPLE)
        pdf.set_font("Helvetica", "", 10)
        pdf.set_text_color(50, 30, 100)
        pdf.set_x(10)
        pdf.multi_cell(0, 7, sanitize(f"  {insights['executive_summary']}"), fill=True)
        pdf.ln(4)

    # ── Key Performance Indicators ───────────────────────────────────────
    if kpis:
        clean_kpis = {k: v for k, v in kpis.items() if k != "_domain_kpis"}
        if clean_kpis:
            pdf.section_title("Key Performance Indicators")
            pdf.kpi_table(clean_kpis)

        domain_kpis = kpis.get("_domain_kpis", {})
        if domain_kpis and isinstance(domain_kpis, dict):
            pdf.sub_title(f"{domain_label} — Smart KPIs")
            pdf.domain_kpi_table(domain_kpis)

    # ── Trends ───────────────────────────────────────────────────────────
    if insights.get("trends"):
        pdf.section_title("Trends Identified", pdf.BG_GREEN)
        pdf.insight_cards(insights["trends"], pdf.BG_GREEN, pdf.GREEN_TEXT)

    # ── Risks ────────────────────────────────────────────────────────────
    if insights.get("risks"):
        pdf.section_title("Risks Identified", pdf.BG_RED)
        pdf.insight_cards(insights["risks"], pdf.BG_RED, pdf.RED)

    # ── Opportunities ─────────────────────────────────────────────────────
    if insights.get("opportunities"):
        pdf.section_title("Opportunities", pdf.BG_YELLOW)
        pdf.insight_cards(insights["opportunities"], pdf.BG_YELLOW, pdf.ORANGE)

    # ── Recommendations ───────────────────────────────────────────────────
    if insights.get("recommendations"):
        pdf.section_title("Recommendations")
        for i, rec in enumerate(insights["recommendations"][:6], 1):
            priority = rec.get("priority", "").upper()
            action   = sanitize(rec.get("action", ""))
            outcome  = sanitize(rec.get("outcome", ""))
