# ── Comparison entry point ──────────────────────────────────────────────────


def generate_comparison_pdf(comparison_result: dict) -> bytes:
    """
    Generate a PDF for either a two-file or multi-file comparison result.

    The function auto-detects the mode by checking for the presence of
    'label1' / 'label2' (two-file) vs 'labels' list with >2 entries (multi).

    Args:
        comparison_result: The full JSON payload returned by /compare or /compare/multi.

    Returns:
        PDF as raw bytes, ready to stream or save.
    """
    pdf = BizlyticsPDF()
    pdf.set_auto_page_break(auto=True, margin=22)

    # ── Detect mode ───────────────────────────────────────────────────────
    if "label1" in comparison_result and "label2" in comparison_result:
        mode   = "two"
        label1 = comparison_result.get("label1", "Dataset A")
        label2 = comparison_result.get("label2", "Dataset B")
        labels = [label1, label2]
    else:
        mode   = "multi"
        labels = comparison_result.get("labels", [])
        label1 = labels[0] if labels else "File 1"
        label2 = labels[1] if len(labels) > 1 else "File 2"

    summary  = comparison_result.get("summary", {})
    metrics_compared    = summary.get("total_metrics_compared", 0)
    significant_changes = summary.get("significant_changes", len(summary.get("improved_metrics", [])) + len(summary.get("declined_metrics", [])))
    file_count = comparison_result.get("file_count", len(labels))
    narrative  = comparison_result.get("narrative", "")

    # ── Cover ─────────────────────────────────────────────────────────────
    pdf.add_page()
    pdf.cover_page_comparison(
        mode=mode,
        labels=labels,
        metrics_compared=metrics_compared,
        significant_changes=significant_changes,
        file_count=file_count,
    )

    # ── Content page(s) ───────────────────────────────────────────────────
    pdf.add_page()


    # ════════════════════════════════════════════════════════════════════
    #  TWO-FILE COMPARISON
    # ════════════════════════════════════════════════════════════════════
    if mode == "two":
        overview = comparison_result.get("overview", {})

        # Overview stats
        pdf.section_title("Comparison Overview")
        stats = [
            (f"{label1} Rows",       overview.get(label1, {}).get("rows", "N/A")),
            (f"{label2} Rows",       overview.get(label2, {}).get("rows", "N/A")),
            ("Metrics Compared",     metrics_compared),
            ("Significant Changes",  significant_changes),
            ("Improved Metrics",     len(summary.get("improved_metrics", []))),
            ("Declined Metrics",     len(summary.get("declined_metrics", []))),
        ]
        pdf.stat_grid(stats)

        # AI Narrative
        if narrative:
            pdf.section_title("AI Comparison Analysis", pdf.BG_PURPLE)
            pdf.set_fill_color(*pdf.BG_PURPLE)
            pdf.set_font("Helvetica", "", 10)
            pdf.set_text_color(50, 30, 100)
            pdf.set_x(10)
            pdf.multi_cell(0, 7, sanitize(f"  {narrative}"), fill=True)
            pdf.ln(4)

        # Improved / declined badge rows
        improved = summary.get("improved_metrics", [])
        declined = summary.get("declined_metrics", [])
        if improved or declined:
            pdf.section_title("Metric Outcomes")
            if improved:
                pdf.comparison_badge_row("Improved", improved, pdf.BG_GREEN, pdf.GREEN_TEXT)
            if declined:
                pdf.comparison_badge_row("Declined", declined, pdf.BG_RED, pdf.RED)

        # Detailed metric breakdown
        metric_rows = comparison_result.get("metric_comparison", [])
        if metric_rows:
            pdf.section_title("Detailed Metric Breakdown")
            pdf.body_text(
                f"Mean values for each shared numeric column across {label1} and {label2}. "
                "Growth % is computed as ((B - A) / |A|) x 100."
            )
            pdf.comparison_metric_table(metric_rows, label1, label2)

        # Column-level overview from overview dict (numeric stats per dataset)
        for ds_label, ds_data in overview.items():
            col_stats = ds_data.get("column_stats", {})
            if not col_stats:
                continue
            pdf.sub_title(f"{ds_label} — Column Statistics")
            pdf.set_font("Helvetica", "B", 8)
            pdf.set_fill_color(*pdf.DARK)
            pdf.set_text_color(*pdf.WHITE)
            for h, w in [("Column", 60), ("Mean", 32), ("Std Dev", 32), ("Min", 28), ("Max", 28), ("Missing%", 10)]:
                pdf.cell(w, 7, h, fill=True, align="C" if h != "Column" else "L")
            pdf.ln()
            pdf.set_font("Helvetica", "", 8)
            for ci, (col, cs) in enumerate(list(col_stats.items())[:15]):
                fc = (248, 250, 255) if ci % 2 == 0 else (255, 255, 255)
                pdf.set_fill_color(*fc)
                pdf.set_text_color(*pdf.DARK)
                pdf.cell(60, 6, sanitize(col.replace("_", " ").title()[:28]), border="B", fill=True)
                pdf.cell(32, 6, fmt_num(cs.get("mean")),  border="B", fill=True, align="R")
                pdf.cell(32, 6, fmt_num(cs.get("std")),   border="B", fill=True, align="R")
                pdf.cell(28, 6, fmt_num(cs.get("min")),   border="B", fill=True, align="R")
                pdf.cell(28, 6, fmt_num(cs.get("max")),   border="B", fill=True, align="R")
                miss_pct = cs.get("missing_pct", 0)
                pdf.set_text_color(*pdf.RED if miss_pct > 10 else pdf.DARK)
                pdf.cell(10, 6, f"{miss_pct:.0f}%", border="B", fill=True, align="C")
                pdf.ln()
            pdf.ln(4)


    # ════════════════════════════════════════════════════════════════════
    #  MULTI-FILE COMPARISON
    # ════════════════════════════════════════════════════════════════════
    else:
        per_file       = comparison_result.get("per_file", [])
        trend_analysis = comparison_result.get("trend_analysis", {})
        growth_rates   = comparison_result.get("growth_rates", {})
        performance    = comparison_result.get("performance", {})
        column_evol    = comparison_result.get("column_evolution", {})
        partial_anal   = comparison_result.get("partial_analysis", [])
        common_cols    = comparison_result.get("common_columns", [])
        selected_col   = common_cols[0] if common_cols else ""

        # Overview stats
        pdf.section_title("Multi-Period Overview")
        stats = [
            ("Files Compared",    file_count),
            ("Metrics Tracked",   metrics_compared),
            ("Improving Metrics", len(summary.get("improved_metrics", []))),
            ("Declining Metrics", len(summary.get("declined_metrics", []))),
            ("Common Columns",    len(common_cols)),
            ("Partial Columns",   len(comparison_result.get("column_evolution", {}).get("partial_columns", []))),
        ]
        pdf.stat_grid(stats)

        # Schema stability banner
        if column_evol:
            pdf.schema_stability_banner(column_evol)

        # AI Narrative
        if narrative:
            pdf.section_title("AI Multi-Period Analysis", pdf.BG_PURPLE)
            pdf.set_fill_color(*pdf.BG_PURPLE)
            pdf.set_font("Helvetica", "", 10)
            pdf.set_text_color(50, 30, 100)
            pdf.set_x(10)
            pdf.multi_cell(0, 7, sanitize(f"  {narrative}"), fill=True)
            pdf.ln(4)

        # Improved / declined
        improved = summary.get("improved_metrics", [])
        declined = summary.get("declined_metrics", [])
        if improved or declined:
            pdf.section_title("Overall Metric Outcomes")
            if improved:
                pdf.comparison_badge_row("Improving Overall", improved, pdf.BG_GREEN, pdf.GREEN_TEXT)
            if declined:
                pdf.comparison_badge_row("Declining Overall", declined, pdf.BG_RED, pdf.RED)

        # Per-file summary cards
        if per_file:
            pdf.section_title("Per-File Summary")
            pdf.multi_file_summary_cards(per_file, selected_col)

        # Trend analysis table
        if trend_analysis:
            pdf.section_title("Mean Trend Across Files")
            pdf.body_text(
                f"Mean value of each shared metric per file. "
                f"'Trend' column compares first vs last file (UP/DN/-)."
            )
            pdf.multi_trend_table(labels, trend_analysis)

        # Growth rates table
        if growth_rates:
            pdf.section_title("Period-over-Period Growth Rates")
            pdf.body_text("Growth % between consecutive files for each shared metric.")
            pdf.multi_growth_table(growth_rates)

        # Performance table
        if performance:
            pdf.section_title("Performance Summary  —  Best / Worst / Total Growth")
            pdf.multi_performance_table(performance)

        # Schema changes per file
        per_period_stats = column_evol.get("per_period_stats", [])
        if per_period_stats:
            pdf.section_title("Schema Changes Per File", pdf.BG_INDIGO)
            pdf.body_text("Each file compared to its predecessor for added/removed columns.")
            pdf.schema_diff_cards(per_period_stats)

        # Column presence matrix
        if column_evol.get("all_columns"):
            pdf.section_title("Column Presence Matrix")
            pdf.body_text(
                "Y = column present in that file, - = absent. "
                "Coverage % shows how many files contain each column."
            )
            pdf.column_presence_matrix(column_evol, labels)

        # Partial columns analysis
        if partial_anal:
            pdf.section_title(f"Partial Column Analysis  ({len(partial_anal)} columns)", pdf.BG_YELLOW)
            pdf.body_text("Columns present in some but not all files, still analysed with available data.")
            for entry in partial_anal[:10]:
                col_name  = sanitize(entry.get("column", "").replace("_", " ").title())
                coverage  = entry.get("coverage_pct", 0)
                avail_in  = entry.get("available_in", [])
                trend     = entry.get("overall_trend", "stable")

                pdf.set_font("Helvetica", "B", 9)
                pdf.set_text_color(*pdf.ORANGE)
                pdf.cell(0, 7,
                    sanitize(f"  {col_name}  —  {coverage}% coverage  ({len(avail_in)} of {file_count} files)  |  Trend: {trend.upper()}"),
                    new_x="LMARGIN", new_y="NEXT")

                per_period = entry.get("per_period", [])
                if per_period:
                    pdf.set_font("Helvetica", "B", 7)
                    pdf.set_fill_color(*pdf.DARK)
                    pdf.set_text_color(*pdf.WHITE)
                    for h, w in [("File", 50), ("Mean", 30), ("Total", 35), ("Std Dev", 30), ("Count", 25), ("Best/Worst", 20)]:
                        pdf.cell(w, 6, h, fill=True, align="C" if h != "File" else "L")
                    pdf.ln()
                    best_p  = entry.get("best_period")
                    worst_p = entry.get("worst_period")
                    pdf.set_font("Helvetica", "", 7)
                    for pi, pp in enumerate(per_period):
                        fc = (248, 250, 255) if pi % 2 == 0 else (255, 255, 255)
                        pdf.set_fill_color(*fc)
                        pdf.set_text_color(*pdf.DARK)
                        pdf.cell(50, 5, sanitize(str(pp.get("label", ""))[:20]), border="B", fill=True)
                        pdf.cell(30, 5, fmt_num(pp.get("mean")),  border="B", fill=True, align="R")
                        pdf.cell(35, 5, fmt_num(pp.get("total")), border="B", fill=True, align="R")
                        pdf.cell(30, 5, fmt_num(pp.get("std")),   border="B", fill=True, align="R")
                        pdf.cell(25, 5, str(int(pp.get("count", 0))), border="B", fill=True, align="R")
                        flag_lbl = pp.get("label", "")
                        if flag_lbl == best_p:
                            pdf.set_text_color(*pdf.GREEN_TEXT)
                            pdf.cell(20, 5, "BEST",  border="B", fill=True, align="C")
                        elif flag_lbl == worst_p:
                            pdf.set_text_color(*pdf.RED)
                            pdf.cell(20, 5, "WORST", border="B", fill=True, align="C")
                        else:
                            pdf.cell(20, 5, "-", border="B", fill=True, align="C")
                        pdf.ln()
                pdf.ln(4)


    # ── Footer note ────────────────────────────────────────────────────────
    pdf.ln(6)
    pdf.set_font("Helvetica", "I", 8)
    pdf.set_text_color(*pdf.LIGHT_GRAY)
    pdf.multi_cell(0, 5,
        "This comparison report was automatically generated by Bizlytics AI. "
        "AI-generated insights should be treated as decision support and validated with domain expertise. "
        f"Report generated: {datetime.now().strftime('%d %B %Y at %I:%M %p')}."
    )

    return bytes(pdf.output())
