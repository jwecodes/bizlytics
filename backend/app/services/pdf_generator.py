# ── Helpers ────────────────────────────────────────────────────────────────


def sanitize(text) -> str:
    if not isinstance(text, str):
        text = str(text)
    replacements = {
        "\u2014": "-", "\u2013": "-",
        "\u2018": "'", "\u2019": "'",
        "\u201c": '"', "\u201d": '"',
        "\u2026": "...", "\u2022": "-",
        "\u25b2": "^", "\u25bc": "v",
        "\u00e9": "e", "\u00e0": "a",
        "\u00fc": "u", "\u00f6": "o",
        "\u00e4": "a", "\u00f1": "n",
        "\u00df": "ss", "\u20ac": "EUR",
        "\u00a3": "GBP", "\u00a5": "JPY",
        "\u2190": "<-", "\u2192": "->",
        "\u2191": "^", "\u2193": "v",
    }
    for char, replacement in replacements.items():
        text = text.replace(char, replacement)
    text = text.encode("latin-1", errors="ignore").decode("latin-1")
    return text



def fmt_num(val) -> str:
    try:
        f = float(val)
        if abs(f) >= 1_000_000:
            return f"{f/1_000_000:.2f}M"
        if abs(f) >= 1_000:
            return f"{f:,.2f}"
        return f"{f:.2f}"
    except (TypeError, ValueError):
        return sanitize(str(val)) if val is not None else "N/A"



